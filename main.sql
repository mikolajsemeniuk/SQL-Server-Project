-- CORRECT PART
drop table if exists users
go

create table users
(
    [user_id] int primary key identity(1, 1),
    [username] varchar(max) not null,
    [sex] bit,
    [birthday] datetime,
    [occupation] varchar(max),
    [created_at] datetime not null default current_timestamp,
    [updated_at] datetime,
)
go

drop table if exists posts
go

create table posts 
(
    [post_id] int primary key identity(1, 1),
    [title] varchar(max) not null,
    [description] varchar(max) not null,
    [image] varchar(max) not null,
    [created_at] datetime not null default current_timestamp,
    [updated_at] datetime,
    [user_id] int foreign key references users([user_id])
)
go

drop table if exists comments
go

create table comments 
(
    [comment_id] int primary key identity(1, 1),
    [content] varchar(max) not null,
    [created_at] datetime not null default current_timestamp,
    [updated_at] datetime,
    [post_id] int foreign key references posts([post_id]),
    [user_id] int foreign key references users([user_id])
)
go

drop table if exists rates
go

create table rates 
(
    [rate_id] int primary key identity(1, 1),
    [rate] int unique not null,
    [created_at] datetime not null default current_timestamp,
    [updated_at] datetime
)
go

drop table if exists user_likes
go

create table user_likes 
(
    following_id int not null,
    follower_id int not null,
    constraint pk_user_like_id primary key (following_id, follower_id),
    constraint fk_user_like_following foreign key (following_id) references users([user_id]),
    constraint fk_user_like_follower foreign key (follower_id) references users([user_id])
)
go

drop table if exists post_likes
go

create table post_likes 
(
    [user_id] int not null,
    [post_id] int not null,
    constraint pk_post_like_id primary key ([user_id], [post_id]),
    constraint fk_post_like_user_id foreign key ([user_id]) references users([user_id]),
    constraint fk_post_like_post_id foreign key ([post_id]) references posts([post_id])
)
go

drop table if exists comment_likes
go

create table comment_likes 
(
    [user_id] int not null,
    [comment_id] int not null,
    constraint pk_comment_like_id primary key ([user_id], [comment_id]),
    constraint fk_comment_like_user_id foreign key ([user_id]) references users([user_id]),
    constraint fk_comment_like_comment_id foreign key ([comment_id]) references comments([comment_id])
)
go

drop table if exists post_rates
go

create table post_rates 
(
    [user_id] int not null,
    [post_id] int not null,
    constraint pk_post_rate_id primary key (user_id, post_id),
    constraint fk_post_rates_user_id foreign key (user_id) references users([user_id]),
    constraint fk_post_rates_post_id foreign key (post_id) references posts([post_id]),
    [rate] int foreign key references rates([rate]) not null
)
go

create trigger [delete_user]
    on users
    instead of delete
as
begin
    set nocount on;

    delete from post_rates where [user_id] in (select [user_id] from deleted)

    delete from comment_likes where [user_id] in (select [user_id] from deleted)
    delete from comments where [user_id] in (select [user_id] from deleted)

    delete from post_likes where [user_id] in (select [user_id] from deleted)
    delete from posts where [user_id] in (select [user_id] from deleted)

    delete from user_likes where (following_id in (select [user_id] from deleted)) or (follower_id in (select [user_id] from deleted))
    delete from users where [user_id] in (select [user_id] from deleted)
end
go

create trigger [delete_post]
    on posts
    instead of delete
as
begin
    set nocount on;

    delete from post_rates where [post_id] in (select [post_id] from deleted)
    
    delete from comments where [post_id] in (select post_id from deleted)

    delete from post_likes where [post_id] in (select post_id from deleted)
    delete from posts where [post_id] in (select post_id from deleted)
end
go

create trigger [delete_comment]
    on comments
    instead of delete
as
begin
    set nocount on;

    delete from comment_likes where comment_id in (select comment_id from deleted)
    delete from comments where comment_id in (select comment_id from deleted)
end
go

create trigger [delete_rates]
    on rates
    instead of delete
as
begin
    set nocount on;

    delete from post_rates where rate in (select rate from deleted)
    delete from rates where rate_id in (select rate_id from deleted)
end
go

